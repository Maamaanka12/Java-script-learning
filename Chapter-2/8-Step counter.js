//Ahmed is using a step counter app. He starts with 10 steps. Every time he walks forward, the step count increases by one. 
// If he walks backward, the step count decreases by one.
//  Write a program to show the step count after moving forward and backward once.

let steps = 10
console.log('Ahmed steps are '+steps)
steps++
console.log('Ahmed moved '+steps + ' steps')
steps--
console.log('Ahmed returned '+steps + ' steps') 